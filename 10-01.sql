DROP VIEW IF EXISTS V;
CREATE VIEW V (proj_id, proj_start, proj_end) AS 
            SELECT  1, TO_DATE('01-JAN-2020', 'DD-MON-YYYY'), TO_DATE('02-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  2, TO_DATE('02-JAN-2020', 'DD-MON-YYYY'), TO_DATE('03-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  3, TO_DATE('03-JAN-2020', 'DD-MON-YYYY'), TO_DATE('04-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  4, TO_DATE('04-JAN-2020', 'DD-MON-YYYY'), TO_DATE('05-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  5, TO_DATE('06-JAN-2020', 'DD-MON-YYYY'), TO_DATE('07-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  6, TO_DATE('16-JAN-2020', 'DD-MON-YYYY'), TO_DATE('17-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  7, TO_DATE('17-JAN-2020', 'DD-MON-YYYY'), TO_DATE('18-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  8, TO_DATE('18-JAN-2020', 'DD-MON-YYYY'), TO_DATE('19-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT  9, TO_DATE('19-JAN-2020', 'DD-MON-YYYY'), TO_DATE('20-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT 10, TO_DATE('21-JAN-2020', 'DD-MON-YYYY'), TO_DATE('22-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT 11, TO_DATE('26-JAN-2020', 'DD-MON-YYYY'), TO_DATE('27-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT 12, TO_DATE('27-JAN-2020', 'DD-MON-YYYY'), TO_DATE('28-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT 13, TO_DATE('28-JAN-2020', 'DD-MON-YYYY'), TO_DATE('29-JAN-2020', 'DD-MON-YYYY')
  UNION ALL SELECT 14, TO_DATE('29-JAN-2020', 'DD-MON-YYYY'), TO_DATE('30-JAN-2020', 'DD-MON-YYYY');

SELECT proj_id, proj_start, proj_end
FROM (
  SELECT proj_id, proj_start, proj_end, LEAD(proj_start) OVER (ORDER BY proj_id) next_proj_start
  FROM V
) T
WHERE proj_end = next_proj_start
