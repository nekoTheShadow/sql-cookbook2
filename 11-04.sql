DROP VIEW IF EXISTS V;
CREATE VIEW V (test1, test2) AS
            SELECT  20,  20
  UNION ALL SELECT  50,  25
  UNION ALL SELECT  20,  20
  UNION ALL SELECT  60,  30
  UNION ALL SELECT  70,  90
  UNION ALL SELECT  80, 130
  UNION ALL SELECT  90,  70
  UNION ALL SELECT 100,  50
  UNION ALL SELECT 110,  55
  UNION ALL SELECT 120,  60
  UNION ALL SELECT 130,  80
  UNION ALL SELECT 140,  70;

SELECT DISTINCT V1.test1, V1.test2
FROM V V1
JOIN V V2 ON V1.test1 = V2.test2 AND V1.test2 = V2.test1
WHERE V1.test1 <= V1.test2
